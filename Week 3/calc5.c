#include <stdio.h>
#include <stdlib.h>

int main() {
    while(1) {
        int left, right;
        char op;

        scanf("%d %c %d", &left, &op, &right);
        if(left == 0 || right = 0) {
            break;
        }
    }

    if(!left || !right) {
        break;
    }

    return EXIT_SUCCESS;
}